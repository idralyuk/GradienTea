package org.hypher.gradientea.animation.shared.group;

import com.google.common.collect.FluentIterable;
import org.hypher.cliententity.shared.entity.ClientEntityAware;
import org.hypher.gradientea.animation.shared.Pixel;
import org.hypher.gradientea.animation.shared.PixelGroupContainer;

import java.util.Set;

/**
 * @author Yona Appletree (yona@concentricsky.com)
 */
public abstract class BasePixelGroupContainer extends BasePixelGroup implements PixelGroupContainer {
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//region// Abstract Methods

	//endregion

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//region// Interface Implementation Methods

	@Override
	protected Iterable<? extends ClientEntityAware> getEntityAwareChildren() {
		return getChildGroups();
	}

	@Override
	public Set<? extends Pixel> getPixels() {
		return FluentIterable.from(getChildGroups())
			.transformAndConcat(getPixels)
			.toSet();
	}

	//endregion

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//region// Protected Methods

	//endregion

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//region// Private Methods

	//endregion

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//region// Getters and Setters

	//endregion

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//region// Inner Classes

	//endregion
}
